function ninjapet_province_select($name='province', $selected='') {
    $html = '<select name="'.esc_attr($name).'" class="np-province-select" required><option value="">انتخاب استان</option>';
    foreach (array_keys(ninjapet_iran_locations()) as $province) $html .= '<option value="'.esc_attr($province).'" '.selected($selected, $province, false).'>'.esc_html($province).'</option>';
    return $html . '</select>';
}

function ninjapet_city_select($name='city', $selected='') {
    $html = '<select name="'.esc_attr($name).'" class="np-city-select" required data-selected="'.esc_attr($selected).'"><option value="">ابتدا استان را انتخاب کنید</option></select>';
    return $html;
}

add_action('wp_enqueue_scripts', function(){
    wp_localize_script('pettt-pro-main', 'ninjapetLocations', ninjapet_iran_locations());
});
